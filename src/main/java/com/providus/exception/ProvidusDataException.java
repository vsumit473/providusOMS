package com.providus.exception;

/**
 * @author SUMIT VERMA
 *
 */
public class ProvidusDataException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 6783019593408751259L;

	public ProvidusDataException() {
		super();
	}

	public ProvidusDataException(String message, Throwable cause) {
		super(message, cause);
	}

	public ProvidusDataException(String message) {
		super(message);
	}

	public ProvidusDataException(Throwable cause) {
		super(cause);
	}
	

}
